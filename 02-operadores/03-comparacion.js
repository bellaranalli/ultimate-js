// estos operadores de comparacion siempre devuelven booleanos

//relacionales
let a = 10;
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

//igualdad
console.log(a == 10); //igual
console.log(a != 10); //distinto
console.log(a === '10'); //mismo valor y mismo tipo de dato
console.log(a !== '10'); //distinto valor y mismo tipo de dato

